"use strict";

const { calculate } = require("../src");
const assert = require("assert");

describe("calculate", () => {
  describe("+ operator", () => {
    it('should return "135478369865209444644" on "12312893712837918273+123165476152371526371"', () => {
      assert.equal(
        calculate("12312893712837918273+123165476152371526371"),
        "135478369865209444644"
      );
    });
  });

  describe("- operator", () => {
    it("should return '54365641872541873526418273440437392215382' on '54365641872541873526418273564187254187728-123749861972346'", () => {
      assert.equal(
        calculate("54365641872541873526418273564187254187728-123749861972346"),
        "54365641872541873526418273440437392215382"
      );
    });
  });

  describe("* operator", () => {
    it("should return '327884449076278692610510327644101610' on '958726459287364598276345987263455 * 342'", () => {
      assert.equal(
        calculate("958726459287364598276345987263455 * 342"),
        "327884449076278692610510327644101610"
      );
    });
  });

  describe("/ operator", () => {
    it("should return '19174529185747291965526919745269' on '95872645928736459827634598726345/5'", () => {
      assert.equal(
        calculate("95872645928736459827634598726345/5"),
        "19174529185747291965526919745269"
      );
    });
  });

  describe("% operator", () => {
    it("should return '29647' on '958726459287364598276345987263455%34234'", () => {
      assert.equal(
        calculate("958726459287364598276345987263455%34234"),
        "29647"
      );
    });
  });

  describe("^ operator", () => {
    it("should return '11101759187078833282262375797622630725872611833441157766157310150916837376' on '1222126^12'", () => {
      assert.equal(
        calculate("1222126^12"),
        "11101759187078833282262375797622630725872611833441157766157310150916837376"
      );
    });
  });

  describe("complex expression", () => {
    it("should return '-158511' on '1231+11123-(343242*2)/4+( (34+344)*2)'", () => {
      assert.equal(
        calculate("1231+11123-(343242*2)/4+( (34+344)*2)"),
        "-158511"
      );
    });
  });
});
